//! 介護現場の略語・専門用語辞書。
//!
//! 要約・構造化の際、記録に含まれる略語や現場用語を LLM が誤解釈しないよう、
//! プロンプトへ注入する参照辞書。RAG のような外部検索は導入せず、頻出かつ
//! 誤読されやすい語を静的辞書として同梱する。
//!
//! 方針:
//! - ここでの用語展開は「転記・整形」の範囲であり、診断ではない。
//!   辞書に無い語や記録に無い事実を補完・推測させないこと（[`super::prompt`] のガードレール参照）。
//! - 疾患名の略語は「記録に書かれた語の読み替え」にのみ用い、
//!   症状から病名を推定するといった医療的判断には使わない。

/// プロンプトへ注入する辞書ブロック（見出し + 本体）。
pub fn glossary_block() -> String {
    format!(
        "# 介護用語辞書（略語・現場用語の解釈に用いる参照。以下の注意を厳守）\n{GUIDANCE}\n{CARE_GLOSSARY}"
    )
}

/// 辞書の使い方に関する注意書き。
const GUIDANCE: &str = "\
- 記録中の略語・現場用語を正しく解釈するためだけに使う。展開は転記・整形であり診断ではない。\n\
- ここに無い略語を勝手に推測して展開しない。不明な語は原文のまま残し、必要なら『確認をお願いします』と促す。\n\
- 略語から病名・重症度・原因などの記録に書かれていない事実を導き出さない。";

